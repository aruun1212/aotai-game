/**
 * 根据地形和天气生成一句行走叙事
 * @param terrains 当前段的地形数组
 * @param weather 当前天气
 * @param rng 随机数生成器
 * @returns 氛围叙事文本，或 null（约 40% 概率不生成，避免刷屏）
 */
export function generateWalkingNarrative(
  terrains: string[],
  weather: string,
  rng: { next(): number; pick<T>(arr: T[]): T },
): string | null {
  // 约 60% 概率生成叙事（每 2-3 格出一句）
  if (rng.next() < 0.4) return null

  const candidates: string[] = []

  // 从地形库中采集
  for (const terrain of terrains) {
    const pool = TERRAIN_NARRATIVES[terrain]
    if (pool && pool.length > 0) {
      candidates.push(...pool)
    }
  }

  // 有 20% 概率混入天气风味
  if (rng.next() < 0.2) {
    const weatherPool = WEATHER_FLAVOR[weather]
    if (weatherPool && weatherPool.length > 0) {
      candidates.push(...weatherPool)
    }
  }

  if (candidates.length === 0) return null

  return rng.pick(candidates)
}
